# An integer is a whole number, a float is a decimal

num1 = 3
num2 = 3.14
print(type(num1))
print(type(num2))

print(3 / 2)
print(3 // 2) # floor devision - drops the decimal

print(3**2) # 3 to the second power

print(5 % 2) # modulus operator - gives the remainder after a division

print(abs(-3)) # absolute value

print(round(3.75, 1))

num_1 = '100'
num_2 = '200'
print(int(num_1) + int(num_2))